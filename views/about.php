				<p class="myfont"> Nkaimurunya Alumni Association started in the year 2014.This was motivated by our pioneer principal Mr Jeremiah Mwangi. Towards the end of that year, there was a school bus fundrasing activity that the newly constituted NKSSAA alumni had to mobilise former students to contribute. After the fundrasing the former students kind of scattered and the NKSSAA went to its knees.A year later a few students visited the school in the bid to rekindled the lost flames. They took pictures and had meetings with the school administration.After that things got quiet untill 2021 when the school again called on the Alumni to help in raising money for a school PA system. This time round the Alumni vowed to start a well structured Alumni that will stand the test of time. The journey began with an election of officials to office and registration of the Alumni Association as a Community-based organisations (CBO).Initial mandate of the Alumni Association will be to giveback and build a massive netwok of Alumni towards the goal of assisting each other.</p>
